let day = +prompt("Hãy nhập một số");

switch (day) {
    case 2:
        document.write("Monday");
        break;
    case 3:
        document.write("Tuesday");
        break;
    case 4:
        document.write("Wednesday");
        break;
    case 5:
        document.write("Thurday");
        break;
    case 6:
        document.write("Friday");
        break;
    case 7:
        document.write("Saturday");
        break;
    case 8:
        document.write("Sunday");
        break;
    default:
        document.write("Không thoả mãn");
        break;
}